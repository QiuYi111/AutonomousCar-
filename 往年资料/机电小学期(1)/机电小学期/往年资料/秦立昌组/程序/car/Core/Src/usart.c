void Check_Rx()
{
	for(uint8_t i = 0;i<Rx_len-1;i++)
	{
		if(Rx_buffer[i]=='A'&&Rx_buffer[i+1]=='T')
		{
			for(uint8_t j = i+2;j<Rx_len-1;j++)
			{
				if(Rx_buffer[j]=='E'&&Rx_buffer[j+1]=='D')
				{
					for(uint8_t k=0;k<Rx_len;k++)  //清空buffer
					{
						if(k>i+1&&k<j)
							Rx_command[k-i-2] = Rx_buffer[k];
                            Rx_buffer[k] = 0;
					}
					Rx_len=0;
					Rx_flag = 1;
					printf("I got the command %s\n",Rx_command);
					break;
				}
			}
		}
		if(Rx_flag)
			break;
	}
	Rx_flag = 0;
}

void Usart_SendString(uint8_t *str)
{
	uint8_t k=0;
	do
	{
		HAL_UART_Transmit(&huart1,(uint8_t *)(str + k) ,1,1000);
		k++;
	}while(*(str + k)!='\0');

}

void clear_command()
{
	for(uint8_t i = 0;i<RXBUFFER_LEN;i++)
		Rx_command[i] = 0;
}


